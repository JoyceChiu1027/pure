package com.bupt.chengde.entity;

public class ResponseSuggest {
	private String suggestId;
	private String suggestModid;
	private String suggestBusid;
	private String posterurl;
	private String suggestTitle;
	private String suggestName;

	public String getSuggestId() {
		return suggestId;
	}

	public void setSuggestId(String suggestId) {
		this.suggestId = suggestId;
	}

	public String getSuggestModid() {
		return suggestModid;
	}

	public void setSuggestModid(String suggestModid) {
		this.suggestModid = suggestModid;
	}

	public String getSuggestBusid() {
		return suggestBusid;
	}

	public void setSuggestBusid(String suggestBusid) {
		this.suggestBusid = suggestBusid;
	}

	public String getPosterurl() {
		return posterurl;
	}

	public void setPosterurl(String posterurl) {
		this.posterurl = posterurl;
	}

	public String getSuggestTitle() {
		return suggestTitle;
	}

	public void setSuggestTitle(String suggestTitle) {
		this.suggestTitle = suggestTitle;
	}

	public String getSuggestName() {
		return suggestName;
	}

	public void setSuggestName(String suggestName) {
		this.suggestName = suggestName;
	}

}

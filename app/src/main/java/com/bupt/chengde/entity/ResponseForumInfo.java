package com.bupt.chengde.entity;

public class ResponseForumInfo {
	private String cardId;
	private String cardTitle;
	private String cardContent;
	private String cardDate;
	private String cardPersonurl;
	private String cardPersonname;

	public String getCardId() {
		return cardId;
	}

	public void setCardId(String cardId) {
		this.cardId = cardId;
	}

	public String getCardTitle() {
		return cardTitle;
	}

	public void setCardTitle(String cardTitle) {
		this.cardTitle = cardTitle;
	}

	public String getCardContent() {
		return cardContent;
	}

	public void setCardContent(String cardContent) {
		this.cardContent = cardContent;
	}

	public String getCardDate() {
		return cardDate;
	}

	public void setCardDate(String cardDate) {
		this.cardDate = cardDate;
	}

	public String getCardPersonurl() {
		return cardPersonurl;
	}

	public void setCardPersonurl(String cardPersonurl) {
		this.cardPersonurl = cardPersonurl;
	}

	public String getCardPersonname() {
		return cardPersonname;
	}

	public void setCardPersonname(String cardPersonname) {
		this.cardPersonname = cardPersonname;
	}

}

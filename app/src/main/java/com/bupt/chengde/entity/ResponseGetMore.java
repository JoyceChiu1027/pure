public class ResponseGetMore {
	private int moreId;
	private String moreTitle;
	private String morePosterUrl;
	private String moreDate;
	private String moreContent;

	public int getMoreId() {
		return moreId;
	}

	public void setMoreId(int moreId) {
		this.moreId = moreId;
	}

	public String getMoreTitle() {
		return moreTitle;
	}

	public void setMoreTitle(String moreTitle) {
		this.moreTitle = moreTitle;
	}

	public String getMorePosterUrl() {
		return morePosterUrl;
	}

	public void setMorePosterUrl(String morePosterUrl) {
		this.morePosterUrl = morePosterUrl;
	}

	public String getMoreDate() {
		return moreDate;
	}

	public void setMoreDate(String moreDate) {
		this.moreDate = moreDate;
	}

	public String getMoreContent() {
		return moreContent;
	}

	public void setMoreContent(String moreContent) {
		this.moreContent = moreContent;
	}

}

package com.bupt.chengde.entity;

public class ResponseSuggestPicture {

	private String suggestId;
	private String suggestUrl;
	private String suggestDetail;
	private String suggestType;

	public String getSuggestId() {
		return suggestId;
	}

	public void setSuggestId(String suggestId) {
		this.suggestId = suggestId;
	}

	public String getSuggestUrl() {
		return suggestUrl;
	}

	public void setSuggestUrl(String suggestUrl) {
		this.suggestUrl = suggestUrl;
	}

	public String getSuggestDetail() {
		return suggestDetail;
	}

	public void setSuggestDetail(String suggestDetail) {
		this.suggestDetail = suggestDetail;
	}

	public String getSuggestType() {
		return suggestType;
	}

	public void setSuggestType(String suggestType) {
		this.suggestType = suggestType;
	}

}

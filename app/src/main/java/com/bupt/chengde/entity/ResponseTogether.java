package com.bupt.chengde.entity;

import android.R.string;

public class ResponseTogether {
	private String togetherId;
	private String togetherTitle;
	private String togetherIntro;
	private String togetherDate;
	private String togetherPersonurl;
	private String togetherPersonName;
	private String togetherPersonCount;

	public String getTogetherId() {
		return togetherId;
	}

	public void setTogetherId(String togetherId) {
		this.togetherId = togetherId;
	}

	public String getTogetherTitle() {
		return togetherTitle;
	}

	public void setTogetherTitle(String togetherTitle) {
		this.togetherTitle = togetherTitle;
	}

	public String getTogetherIntro() {
		return togetherIntro;
	}

	public void setTogetherIntro(String togetherIntro) {
		this.togetherIntro = togetherIntro;
	}

	public String getTogetherDate() {
		return togetherDate;
	}

	public void setTogetherDate(String togetherDate) {
		this.togetherDate = togetherDate;
	}

	public String getTogetherPersonurl() {
		return togetherPersonurl;
	}

	public void setTogetherPersonurl(String togetherPersonurl) {
		this.togetherPersonurl = togetherPersonurl;
	}

	public String getTogetherPersonName() {
		return togetherPersonName;
	}

	public void setTogetherPersonName(String togetherPersonName) {
		this.togetherPersonName = togetherPersonName;
	}

	public String getTogetherPersonCount() {
		return togetherPersonCount;
	}

	public void setTogetherPersonCount(String togetherPersonCount) {
		this.togetherPersonCount = togetherPersonCount;
	}

}

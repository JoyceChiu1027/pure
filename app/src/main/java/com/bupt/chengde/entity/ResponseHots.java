package com.bupt.chengde.entity;

public class ResponseHots {
	private int hotId;
	private String hotTitle;
	private String hotDate;
	private String hotPosterUrl;

	public int getHotId() {
		return hotId;
	}

	public void setHotId(int hotId) {
		this.hotId = hotId;
	}

	public String getHotTitle() {
		return hotTitle;
	}

	public void setHotTitle(String hotTitle) {
		this.hotTitle = hotTitle;
	}

	public String getHotDate() {
		return hotDate;
	}

	public void setHotDate(String hotDate) {
		this.hotDate = hotDate;
	}

	public String getHotPosterUrl() {
		return hotPosterUrl;
	}

	public void setHotPosterUrl(String hotPosterUrl) {
		this.hotPosterUrl = hotPosterUrl;
	}

}

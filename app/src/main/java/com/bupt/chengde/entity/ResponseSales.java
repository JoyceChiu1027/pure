package com.bupt.chengde.entity;

public class ResponseSales {
	private int saleId;
	private String saleTitle;
	private String salePosterUrl;
	private String saleDate;

	public int getSaleId() {
		return saleId;
	}

	public void setSaleId(int saleId) {
		this.saleId = saleId;
	}

	public String getSaleTitle() {
		return saleTitle;
	}

	public void setSaleTitle(String saleTitle) {
		this.saleTitle = saleTitle;
	}

	public String getSalePosterUrl() {
		return salePosterUrl;
	}

	public void setSalePosterUrl(String salePosterUrl) {
		this.salePosterUrl = salePosterUrl;
	}

	public String getSaleDate() {
		return saleDate;
	}

	public void setSaleDate(String saleDate) {
		this.saleDate = saleDate;
	}

}

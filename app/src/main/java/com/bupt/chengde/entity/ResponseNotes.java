package com.bupt.chengde.entity;

public class ResponseNotes {
	private String noteId;
	private String noteTitle;
	private String noteIntro;
	private String noteDate;
	private String notePersonurl;
	private String notePersonname;

	public String getNoteId() {
		return noteId;
	}

	public void setNoteId(String noteId) {
		this.noteId = noteId;
	}

	public String getNoteTitle() {
		return noteTitle;
	}

	public void setNoteTitle(String noteTitle) {
		this.noteTitle = noteTitle;
	}

	public String getNoteIntro() {
		return noteIntro;
	}

	public void setNoteIntro(String noteIntro) {
		this.noteIntro = noteIntro;
	}

	public String getNoteDate() {
		return noteDate;
	}

	public void setNoteDate(String noteDate) {
		this.noteDate = noteDate;
	}

	public String getNotePersonurl() {
		return notePersonurl;
	}

	public void setNotePersonurl(String notePersonurl) {
		this.notePersonurl = notePersonurl;
	}

	public String getNotePersonname() {
		return notePersonname;
	}

	public void setNotePersonname(String notePersonname) {
		this.notePersonname = notePersonname;
	}

}

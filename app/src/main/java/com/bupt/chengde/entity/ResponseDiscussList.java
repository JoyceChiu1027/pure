package com.bupt.chengde.entity;

public class ResponseDiscussList {
	private String discussId;
	private String discussCustId;
	private String content;
	private String discussDate;
	private String nickName;
	private String phoneNo;

	public String getDiscussId() {
		return discussId;
	}

	public void setDiscussId(String discussId) {
		this.discussId = discussId;
	}

	public String getDiscussCustId() {
		return discussCustId;
	}

	public void setDiscussCustId(String discussCustId) {
		this.discussCustId = discussCustId;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public String getDiscussDate() {
		return discussDate;
	}

	public void setDiscussDate(String discussDate) {
		this.discussDate = discussDate;
	}

	public String getNickName() {
		return nickName;
	}

	public void setNickName(String nickName) {
		this.nickName = nickName;
	}

	public String getPhoneNo() {
		return phoneNo;
	}

	public void setPhoneNo(String phoneNo) {
		this.phoneNo = phoneNo;
	}

}
